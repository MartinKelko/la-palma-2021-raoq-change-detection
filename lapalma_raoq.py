def remove_raoq_classic_prefix(
    name
):

    prefixes = [
        "BIN_RaoQ_classic_",
        "BIN_RAOQ_CLASSIC_",
        "BIN_RaoQ_Classic_",
        "BIN_RAOQ_Classic_",
        "BIN_RaoQ_",
        "BIN_RAOQ_",
        "BIN_",
    ]

    for prefix in prefixes:
        if name.startswith(prefix):
            return name[
                len(prefix):
            ]

    return None


def parse_prediction_column(
    column_name
):

    original_name = str(
        column_name
    ).strip()

    name = remove_raoq_classic_prefix(
        original_name
    )

    if name is None:
        return None

    match = re.search(
        r"_(1|2)1$",
        name
    )

    if match is None:
        match = re.search(
            r"_(1|2)$",
            name
        )

    if match is None:
        return None

    pair_id = match.group(1)

    variable = name[
        :match.start()
    ]

    variable = re.sub(
        r"^(RaoQ_classic_|RAOQ_CLASSIC_|RaoQ_|RAOQ_)",
        "",
        variable
    )

    if pair_id == "1":
        pre_period = "PRE_1"
        post_period = "POST_1"

    else:
        pre_period = "PRE_2"
        post_period = "POST_2"

    return {
        "variable_raw": variable,
        "pair_id": pair_id,
        "pair": (
            f"{pre_period}_TO_{post_period}"
        ),
        "pre_period": pre_period,
        "pre_date": np.nan,
        "post_period": post_period,
        "post_date": np.nan,
    }


