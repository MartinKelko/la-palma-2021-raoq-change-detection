def get_binary_threshold(
    arr,
    meta,
    method,
    lava_geom_proj=None,
    var_name=None
):

    DEFAULT_MIN_SAMPLES = 50

    MIN_SAMPLES_BY_VARIABLE = {
        "Landsat_TIR": 20,
        "MODIS": 20,
        "VIIRS_NT": 10,
        "VIIRS_BM": 10,
    }

    THRESHOLD_REGION_BY_VARIABLE = {
        "MODIS": "analysis",
    }

    FALLBACK_PERCENTILE_BY_VARIABLE = {
        "MODIS": 90,
    }

    min_samples = MIN_SAMPLES_BY_VARIABLE.get(
        var_name,
        DEFAULT_MIN_SAMPLES
    )

    threshold_region = THRESHOLD_REGION_BY_VARIABLE.get(
        var_name,
        THRESHOLD_CALIBRATION_REGION
    )

    if threshold_region == "lava":

        if lava_geom_proj is None:
            raise ValueError(
                "lava_geom_proj is required for lava threshold calibration"
            )

        values = values_inside_geometry(
            arr,
            meta,
            lava_geom_proj
        )

        region_label = "CEMS lava-flow polygon"

    elif threshold_region == "analysis":

        values = arr[
            np.isfinite(arr)
        ]

        region_label = "complete analysis buffer"

    else:
        raise ValueError(
            "Threshold region must be 'lava' or 'analysis'"
        )

    values = np.asarray(
        values,
        dtype="float64"
    )

    values = values[
        np.isfinite(values)
    ]

    if values.size < min_samples:
        raise ValueError(
            f"Too few valid threshold pixels: "
            f"{values.size} < {min_samples}"
        )

    unique_values = np.unique(values)

    if unique_values.size < 2:
        raise ValueError(
            "Threshold cannot be calculated because all valid "
            "pixels have the same value."
        )

    threshold = get_threshold(
        values,
        method=method
    )

    threshold_method_used = method



    if not np.isfinite(threshold):

        fallback_percentile = (
            FALLBACK_PERCENTILE_BY_VARIABLE.get(
                var_name
            )
        )

        if fallback_percentile is None:
            raise ValueError(
                f"Threshold calculation returned {threshold}"
            )

        threshold = float(
            np.nanpercentile(
                values,
                fallback_percentile
            )
        )

        threshold_method_used = (
            f"percentile_{fallback_percentile}_fallback"
        )

        print(
            f"WARNING: {method} threshold failed for {var_name}. "
            f"Using {fallback_percentile}th percentile fallback."
        )

    if not np.isfinite(threshold):
        raise ValueError(
            f"Fallback threshold calculation returned {threshold}"
        )

    print(
        f"Threshold samples: {values.size} | "
        f"unique values: {unique_values.size}"
    )

    print(
        f"Threshold method actually used: "
        f"{threshold_method_used}"
    )

    return threshold, region_label


