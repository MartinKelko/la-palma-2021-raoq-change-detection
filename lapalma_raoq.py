def multidim_change_detection(combo_vars):
    tag = combo_tag(combo_vars)

    print(f"\nCHANGE DETECTION RaoQ multidimensional {tag}")

    pre, meta = read_tif(
        _p(f"PRE_RaoQ_MULTIDIM_{tag}.tif")
    )

    for y in POST_YEARS:

        post, _ = read_tif(
            _p(f"RaoQ_MULTIDIM_{tag}_{y}.tif")
        )

        dist = absdiff_arrays(post, pre)

        write_tif(
            _p(f"DIST_TO_BASELINE_RaoQ_MULTIDIM_{tag}_{y}_ABS.tif"),
            dist,
            meta
        )

        print(f"DIST_TO_BASELINE_RaoQ_MULTIDIM_{tag}_{y}_ABS")


def multidim_binary_maps(combo_vars):
    tag = combo_tag(combo_vars)

    print("\n" + "="*60)
    print(f"BINARY MAPS RaoQ multidimensional {tag}")
    print("="*60)

    for y in POST_YEARS:

        in_path = _p(
            f"DIST_TO_BASELINE_RaoQ_MULTIDIM_{tag}_{y}_ABS.tif"
        )

        out_path = _p(
            f"BINARY_RaoQ_MULTIDIM_{tag}_{y}_TRIANGLE.tif"
        )

        if not os.path.exists(in_path):
            print("Missing input:", os.path.basename(in_path))
            continue

        arr, meta = read_raster(in_path)

        thr = triangle_threshold(arr, TRIANGLE_NBINS)

        binary = apply_threshold(arr, thr)

        write_binary(out_path, binary, meta)

        print(f"BINARY_RaoQ_MULTIDIM_{tag}_{y}_TRIANGLE")


def multidim_maps_and_tables(combo_vars, title_label=None):

    tag = combo_tag(combo_vars)

    if title_label is None:
        title_label = combo_label(combo_vars)

    print("\n" + "="*60)
    print(f"TABLE AND VISUALIZATION RaoQ multidimensional {tag}")
    print("="*60)

    rows = []
    out_map = {}

    candidate_paths = [
        _p(f"BINARY_RaoQ_MULTIDIM_{tag}_{y}_TRIANGLE.tif")
        for y in POST_YEARS
    ]

    existing_candidates = [
        p for p in candidate_paths if os.path.exists(p)
    ]

    if len(existing_candidates) == 0:
        raise FileNotFoundError(
            f"No multidimensional RaoQ binary rasters found for {tag}"
        )

    lava_geom_proj = get_lava_geom_for_raster(
        existing_candidates[0]
    )

    for y in POST_YEARS:

        path = _p(
            f"BINARY_RaoQ_MULTIDIM_{tag}_{y}_TRIANGLE.tif"
        )

        if not os.path.exists(path):
            print("Missing:", os.path.basename(path))
            continue

        binary, meta = read_raster(path)

        n_valid, n_change, share, area_ha = summarize_binary_in_lava(
            binary,
            meta,
            lava_geom_proj=lava_geom_proj
        )

        rows.append({
            "year": y,
            "approach": "RaoQ_multidimensional",
            "variables": combo_label(combo_vars),
            "binary_output": os.path.basename(path),
            "n_valid": n_valid,
            "n_change": n_change,
            "share_change_in_lava_%": share,
            "area_ha_in_lava": area_ha
        })

        out_map[y] = path

        print(f"BINARY_RaoQ_MULTIDIM_{tag}_{y}_TRIANGLE")

    df = (
        pd.DataFrame(rows)
        .sort_values(["year"])
        .reset_index(drop=True)
    )

    display(df)

    plot_vertical_block(
        [out_map.get(y) for y in POST_YEARS],
        lava_geom_proj,
        f"{title_label}: RaoQ multidimensional binary change maps"
    )

    return df

