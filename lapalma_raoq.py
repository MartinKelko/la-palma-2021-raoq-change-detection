def classify_prediction_column(col_name):
    name = col_name.upper()

    # remove prefix
    if name.startswith("BIN_"):
        name = name[4:]

    # remove year
    for y in ["_2021", "_2022", "_2023", "_2024", "_2025"]:
        name = name.replace(y, "")

    # ---- bands ----
    if "S1_VV" in name:
        return "Sentinel-1", "SAR", "S1_VV", "band"

    if "S1_VH" in name:
        return "Sentinel-1", "SAR", "S1_VH", "band"

    if name == "B8A":
        return "Sentinel-2", "MSI", "B8A", "band"

    if name == "B11":
        return "Sentinel-2", "MSI", "B11", "band"

    if name == "B12":
        return "Sentinel-2", "MSI", "B12", "band"

    if "TIR" in name:
        return "Landsat", "TIRS", "Landsat_TIR", "band"

    if "MODIS" in name:
        return "Terra/Aqua", "MODIS", "MODIS_LST", "band"

    if "VIIRS_NT" in name:
        return "Suomi NPP / NOAA-20", "VIIRS", "VIIRS_NT", "band"

    if "VIIRS_BM" in name:
        return "Suomi NPP / NOAA-20", "VIIRS", "VIIRS_BM", "band"

    # ---- indices ----
    if "NDVI" in name:
        return "Sentinel-2", "MSI", "NDVI", "index"

    if "NBR" in name:
        return "Sentinel-2", "MSI", "NBR", "index"

    if "MIRBI" in name:
        return "Sentinel-2", "MSI", "MIRBI", "index"

    if "BAIS2" in name:
        return "Sentinel-2", "MSI", "BAIS2", "index"

    if "NHI_SWNIR" in name:
        return "Sentinel-2", "MSI", "NHI_SWNIR", "index"

    if "NHI_SWIR" in name:
        return "Sentinel-2", "MSI", "NHI_SWIR", "index"

