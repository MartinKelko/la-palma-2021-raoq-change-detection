from shapely.geometry import shape, mapping
from shapely.ops import unary_union


def normalize_geojson_geometry(geometry_info):

    if geometry_info is None:
        raise ValueError("Earth Engine geometry is None.")

    geometry_type = geometry_info.get("type")

    if geometry_type in [
        "Point",
        "MultiPoint",
        "LineString",
        "MultiLineString",
        "Polygon",
        "MultiPolygon"
    ]:
        if "coordinates" not in geometry_info:
            raise ValueError(
                f"Geometry type {geometry_type} has no coordinates."
            )

        return geometry_info

    if geometry_type == "GeometryCollection":

        components = geometry_info.get(
            "geometries",
            []
        )

        polygon_components = []

        for component in components:

            component_type = component.get("type")

            if component_type in [
                "Polygon",
                "MultiPolygon"
            ]:
                polygon_components.append(
                    shape(component)
                )

            elif component_type == "GeometryCollection":
                nested = normalize_geojson_geometry(
                    component
                )

                nested_shape = shape(nested)

                if not nested_shape.is_empty:
                    polygon_components.append(
                        nested_shape
                    )

        if len(polygon_components) == 0:
            raise ValueError(
                "GeometryCollection contains no Polygon or "
                "MultiPolygon components."
            )

        dissolved = unary_union(
            polygon_components
        )

        if dissolved.is_empty:
            raise ValueError(
                "Dissolved polygon geometry is empty."
            )

        if not dissolved.is_valid:
            dissolved = dissolved.buffer(0)

        normalized = mapping(
            dissolved
        )

        if normalized.get("type") not in [
            "Polygon",
            "MultiPolygon"
        ]:
            raise ValueError(
                "GeometryCollection could not be converted "
                "to a polygonal geometry."
            )

        return normalized

    raise ValueError(
        f"Unsupported Earth Engine geometry type: {geometry_type}"
    )


def ee_geometry_to_raster_crs(
    ee_geom,
    target_crs
):


    if target_crs is None:
        raise ValueError(
            "Target raster CRS is missing."
        )

    geometry_info = ee_geom.getInfo()

    normalized_geometry = normalize_geojson_geometry(
        geometry_info
    )

    transformed_geometry = transform_geom(
        src_crs="EPSG:4326",
        dst_crs=target_crs,
        geom=normalized_geometry,
        precision=6
    )

    if transformed_geometry.get("type") in [
        "Polygon",
        "MultiPolygon"
    ]:
        if "coordinates" not in transformed_geometry:
            raise ValueError(
                "Transformed geometry has no coordinates."
            )

    return transformed_geometry


def get_reference_geometries_for_raster(path):

    _, meta = read_raster(
        path
    )

    raster_crs = meta.get(
        "crs"
    )

    if raster_crs is None:
        raise ValueError(
            f"Raster has no CRS: {path}"
        )

    required_geometries = [
        "lava_geom",
        "analysis_geom",
        "no_change_geom"
    ]

    missing_geometries = [
        geometry_name
        for geometry_name in required_geometries
        if geometry_name not in globals()
    ]

    if missing_geometries:
        raise NameError(
            "Missing AOI geometries: "
            + ", ".join(missing_geometries)
            + ". Run Section 1 DATA ACQUISITION AND AOI first."
        )

    target_crs = raster_crs.to_string()

    geometries = {}

    geometry_sources = {
        "lava": lava_geom,
        "no_change": no_change_geom,
        "analysis": analysis_geom
    }

    for geometry_name, geometry_source in geometry_sources.items():

        try:
            geometries[geometry_name] = (
                ee_geometry_to_raster_crs(
                    geometry_source,
                    target_crs
                )
            )

        except Exception as error:
            raise RuntimeError(
                f"Failed to transform '{geometry_name}' geometry "
                f"to raster CRS {target_crs}: "
                f"{type(error).__name__}: {error}"
            ) from error

    return geometries


def get_lava_geom_for_raster(path):

    return get_reference_geometries_for_raster(
        path
    )["lava"]


def geometry_to_mask(
    geometry,
    meta,
    out_shape
):


    if geometry is None:
        return np.zeros(
            out_shape,
            dtype=bool
        )

    geometry_type = geometry.get(
        "type"
    )

    if geometry_type not in [
        "Polygon",
        "MultiPolygon"
    ]:
        raise ValueError(
            f"geometry_to_mask received unsupported geometry "
            f"type: {geometry_type}"
        )

    if "coordinates" not in geometry:
        raise ValueError(
            f"{geometry_type} geometry has no coordinates."
        )

    return geometry_mask(
        [geometry],
        transform=meta["transform"],
        invert=True,
        out_shape=out_shape
    )


def values_inside_geometry(
    arr,
    meta,
    geometry
):
    """
    Return finite raster values inside a reference polygon.
    """

    inside_mask = geometry_to_mask(
        geometry,
        meta,
        arr.shape
    )

    valid_mask = (
        inside_mask
        & np.isfinite(arr)
    )

    return arr[
        valid_mask
    ]


