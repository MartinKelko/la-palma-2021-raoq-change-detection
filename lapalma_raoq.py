def normalize_fixed_range(arr, vmin, vmax, clip=True):
    if vmax <= vmin:
        raise ValueError("vmax must be greater than vmin")

    output = arr.astype("float32").copy()
    valid = np.isfinite(output)

    if clip:
        output[valid] = np.clip(output[valid], vmin, vmax)

    output[valid] = (
        (output[valid] - vmin)
        / (vmax - vmin)
    )

    return output


def open_normalized_multidim_inputs(period, vars_list):
    temporary_paths = []
    reference_arr = None
    reference_meta = None

    for index, var_name in enumerate(vars_list):
        source_path = input_path(var_name, period)

        if not os.path.exists(source_path):
            raise FileNotFoundError(source_path)

        if var_name not in MD_NORMALIZATION_RULES:
            raise ValueError(
                f"No multidimensional normalization rule for {var_name}"
            )

        arr, meta = read_tif(source_path)

        if index == 0:
            reference_arr = arr
            reference_meta = meta.copy()
            aligned = arr
        else:
            if not same_grid(
                reference_meta,
                reference_arr,
                meta,
                arr
            ):
                aligned = align_to_reference(
                    arr,
                    meta,
                    reference_meta,
                    resampling=Resampling.bilinear
                )
            else:
                aligned = arr

        rule = MD_NORMALIZATION_RULES[var_name]

        normalized = normalize_fixed_range(
            aligned,
            vmin=rule["vmin"],
            vmax=rule["vmax"],
            clip=True
        )

        temporary_path = _p(
            f"_TMP_MD_{var_name}_{period}_"
            f"{image_date_tag(var_name, period)}_NORM.tif"
        )

        write_tif(
            temporary_path,
            normalized,
            reference_meta
        )

        temporary_paths.append(temporary_path)

    return temporary_paths


def spectralrao_multidim_array(
    mats,
    window=9,
    distance="euclidean",
    na_tolerance=0.3,
    min_valid_override=None
):
    if window % 2 != 1:
        raise ValueError("window must be odd")

    if len(mats) < 2:
        raise ValueError("Provide at least two dimensions")

    mats = [np.asarray(mat, dtype="float64") for mat in mats]

    height, width = mats[0].shape

    for mat in mats[1:]:
        if mat.shape != (height, width):
            raise ValueError("All multidimensional arrays must have the same shape")

    half_window = (window - 1) // 2
    window_size = window * window

    min_valid = int(
        np.ceil(window_size * (1.0 - float(na_tolerance)))
    )

    if min_valid_override is not None:
        min_valid = int(min_valid_override)

    min_valid = max(2, min_valid)

    output = np.full((height, width), np.nan, dtype="float64")

    padded = []

    for mat in mats:
        pad = np.full(
            (height + 2 * half_window, width + 2 * half_window),
            np.nan,
            dtype="float64"
        )

        pad[
            half_window:half_window + height,
            half_window:half_window + width
        ] = mat

        padded.append(pad)

    for row in range(half_window, height + half_window):
        for col in range(half_window, width + half_window):
            windows = [
                dimension[
                    row - half_window:row + half_window + 1,
                    col - half_window:col + half_window + 1
                ].ravel()
                for dimension in padded
            ]

            vectors = np.stack(windows, axis=1)
            valid = np.all(np.isfinite(vectors), axis=1)
            count = int(valid.sum())

            if count < min_valid:
                continue

            values = vectors[valid]

            if distance == "euclidean":
                differences = values[:, None, :] - values[None, :, :]
                distances = np.sqrt(
                    np.sum(differences * differences, axis=-1)
                )
            elif distance == "manhattan":
                differences = np.abs(
                    values[:, None, :] - values[None, :, :]
                )
                distances = np.sum(differences, axis=-1)
            else:
                raise ValueError(
                    "distance must be 'euclidean' or 'manhattan'"
                )

            upper_triangle = np.triu_indices(count, k=1)
            sum_upper = float(distances[upper_triangle].sum())

            output[
                row - half_window,
                col - half_window
            ] = (2.0 * sum_upper) / (count * count)

    return output.astype("float32")


def spectralrao_multidim(
    input_paths,
    output_path,
    window=9,
    distance="euclidean",
    na_tolerance=0.3,
    min_valid_override=None
):
    if len(input_paths) < 2:
        raise ValueError("Provide at least two input rasters")

    matrices = []
    reference_meta = None

    for index, path in enumerate(input_paths):
        arr, meta = read_tif(path)
        matrices.append(arr)

        if index == 0:
            reference_meta = meta.copy()

    output = spectralrao_multidim_array(
        matrices,
        window=window,
        distance=distance,
        na_tolerance=na_tolerance,
        min_valid_override=min_valid_override
    )

    write_tif(output_path, output, reference_meta)

    print(
        "Saved RaoQ multidimensional:",
        os.path.basename(output_path)
    )

    return output_path


