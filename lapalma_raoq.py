def normalize_fixed_range(
    array,
    vmin,
    vmax,
    clip=True
):
    if vmax <= vmin:
        raise ValueError(
            f"Invalid normalization range: "
            f"{vmin} to {vmax}"
        )

    output = array.astype(
        "float32"
    ).copy()

    valid = np.isfinite(
        output
    )

    if clip:
        output[valid] = np.clip(
            output[valid],
            vmin,
            vmax
        )

    output[valid] = (
        output[valid] - vmin
    ) / (
        vmax - vmin
    )

    return output


def open_normalized_multidim_inputs(
    period,
    vars_list
):
    temporary_paths = []

    reference_meta = None
    reference_array = None

    for index, var_name in enumerate(
        vars_list
    ):
        source_path = md_input_path(
            var_name,
            period
        )

        if not os.path.exists(
            source_path
        ):
            raise FileNotFoundError(
                f"Missing input raster: {source_path}"
            )

        array, metadata = read_raster(
            source_path
        )

        if index == 0:
            reference_meta = metadata.copy()
            reference_array = array.copy()

            aligned_array = array
            aligned_meta = metadata.copy()

        elif not same_grid(
            reference_meta,
            reference_array,
            metadata,
            array
        ):
            aligned_array = align_to_reference(
                array,
                metadata,
                reference_meta,
                resampling=Resampling.bilinear
            )

            aligned_meta = reference_meta.copy()

        else:
            aligned_array = array
            aligned_meta = metadata.copy()

        rule = md_normalization_rule(
            var_name
        )

        normalized = normalize_fixed_range(
            aligned_array,
            vmin=rule["vmin"],
            vmax=rule["vmax"],
            clip=True
        )

        temporary_path = _p(
            f"_TMP_MD_{var_name}_"
            f"{period}_"
            f"{md_date_tag(var_name, period)}_NORM.tif"
        )

        write_tif(
            temporary_path,
            normalized,
            aligned_meta
        )

        temporary_paths.append(
            temporary_path
        )

    return temporary_paths


