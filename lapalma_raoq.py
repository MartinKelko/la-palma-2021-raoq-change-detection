def parse_md_prediction_column(
    column_name
):
    """
    Supported examples:

    BIN_B8A_B11_11
    BIN_B8A_B11_21

    BIN_RaoQ_MD_B8A_B11_11
    BIN_RaoQ_MD_B8A_B11_21

    BIN_RaoQ_MULTIDIM_B8A_B11_11
    """

    name = str(
        column_name
    ).strip()

    valid_prefixes = [
        "BIN_RaoQ_MULTIDIM_",
        "BIN_RAOQ_MULTIDIM_",
        "BIN_RaoQ_MD_",
        "BIN_RAOQ_MD_",
        "BIN_",
    ]

    matched_prefix = None

    for prefix in valid_prefixes:

        if name.startswith(prefix):
            matched_prefix = prefix
            break

    if matched_prefix is None:
        return None

    name = name[
        len(matched_prefix):
    ]

    match = re.search(
        r"_(1|2)1$",
        name
    )

    if match is None:
        match = re.search(
            r"_(1|2)$",
            name
        )

    if match is None:
        return None

    pair_id = match.group(1)

    combination_raw = name[
        :match.start()
    ]

    variables = split_combination_variables(
        combination_raw
    )

    if pair_id == "1":
        pre_period = "PRE_1"
        post_period = "POST_1"

    else:
        pre_period = "PRE_2"
        post_period = "POST_2"

    contains_sar = any(
        variable.startswith("S1_")
        for variable in variables
    )

    return {
        "combination_raw": combination_raw,
        "combination": " + ".join(
            variables
        ),
        "variables": variables,
        "dimension": len(variables),
        "contains_SAR": contains_sar,
        "pair_id": pair_id,
        "pair": (
            f"{pre_period}_TO_{post_period}"
        ),
        "pre_period": pre_period,
        "pre_date": np.nan,
        "post_period": post_period,
        "post_date": np.nan,
    }


