def write_binary_md(
    path,
    arr,
    meta,
    create_sampling_copy=True
):
    import os
    import numpy as np
    import rasterio

    original_arr = np.asarray(
        arr,
        dtype="float32"
    )

    write_tif(
        path,
        original_arr,
        meta,
        nodata=NODATA_OUT,
        dtype="float32",
        compress="deflate"
    )

    sampling_path = None

    if create_sampling_copy:

        sampling_path = (
            os.path.splitext(path)[0]
            + "_SAMPLING.tif"
        )

        sampling_arr = np.zeros(
            original_arr.shape,
            dtype="uint8"
        )

        valid = np.isfinite(
            original_arr
        )

        if NODATA_OUT is not None:
            valid &= (
                original_arr
                != NODATA_OUT
            )

        sampling_arr[
            valid
        ] = (
            original_arr[valid] >= 0.5
        ).astype("uint8")

        sampling_meta = meta.copy()

        sampling_meta.update({
            "dtype": "uint8",
            "count": 1,
            "nodata": None,
            "compress": "deflate"
        })


        with rasterio.open(
            sampling_path,
            "w",
            **sampling_meta
        ) as dataset:

            dataset.write(
                sampling_arr,
                1
            )

        print(
            "Saved MD binary:",
            os.path.basename(path)
        )

        print(
            "Saved MD sampling binary:",
            os.path.basename(sampling_path)
        )

    return {
        "binary_path": path,
        "sampling_path": sampling_path
    }

