def write_tif(
    path,
    arr,
    meta,
    nodata=NODATA_OUT,
    dtype="float32",
    compress="deflate"
):
    meta_out = meta.copy()
    meta_out.pop("compress", None)

    meta_out.update(
    count=1,
    dtype=dtype,
    compress=compress
)

    if nodata is not None:
        meta_out["nodata"] = float(nodata)

    out = np.asarray(arr, dtype=dtype).copy()

    if nodata is not None:
        out[~np.isfinite(out)] = float(nodata)

        out = np.asarray(arr, dtype=dtype).copy()
        out[~np.isfinite(out)] = float(nodata)

        with rasterio.open(path, "w", **meta_out) as dst:
            dst.write(out, 1)

        return path


