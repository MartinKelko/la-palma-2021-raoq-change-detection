def summarize_binary_region(
    binary,
    meta,
    region_geom_proj,
    region_name
):
    region_mask = geometry_to_mask(
        region_geom_proj,
        meta,
        binary.shape
    )

    valid = region_mask & np.isfinite(binary)

    n_valid = int(valid.sum())
    n_change = int(np.sum(binary[valid] == 1))
    n_no_change = int(np.sum(binary[valid] == 0))

    pixel_area_m2 = abs(
        float(meta["transform"].a)
        * float(meta["transform"].e)
    )

    change_area_ha = (
        n_change * pixel_area_m2 / 10000.0
    )

    no_change_area_ha = (
        n_no_change * pixel_area_m2 / 10000.0
    )

    share_change = (
        100.0 * n_change / n_valid
        if n_valid > 0
        else np.nan
    )

    share_no_change = (
        100.0 * n_no_change / n_valid
        if n_valid > 0
        else np.nan
    )

    return {
        "region": region_name,
        "n_valid": n_valid,
        "n_change": n_change,
        "n_no_change": n_no_change,
        "share_change_%": share_change,
        "share_no_change_%": share_no_change,
        "change_area_ha": change_area_ha,
        "no_change_area_ha": no_change_area_ha
    }


