def _normalize_approach(approach):
    value = str(approach).strip().lower()

    aliases = {
        "spectral": "spectral",
        "direct": "spectral",
        "direct_spectral": "spectral",

        "raoq": "raoq",
        "rao": "raoq",
        "classic_raoq": "raoq",
        "raoq_classic": "raoq",
    }

    if value not in aliases:
        raise ValueError(
            "Unknown approach. Use 'spectral' or 'raoq'."
        )

    return aliases[value]


def check_input_rasters(var_name):
    ensure_selected_dates(var_name)

    missing = [
        input_path(var_name, period)
        for period in PERIODS
        if not os.path.exists(input_path(var_name, period))
    ]

    if missing:
        raise FileNotFoundError(
            f"Missing source rasters for {var_name}:\n"
            + "\n".join(f" - {path}" for path in missing)
        )


def run_variable_workflow(
    var_name,
    approach,
    title_label=None,
    prepare_data=True,
    na_tolerance=0.3,
    overwrite_preparation=True,
    overwrite_change=True,
    overwrite_binary=True,
    make_outputs=True
):
    approach = _normalize_approach(approach)
    title_label = title_label or var_name

    check_input_rasters(var_name)

    print("\n" + "=" * 72)
    print(f"{approach.upper()} WORKFLOW: {var_name}")
    print("=" * 72)

    if prepare_data:
        if approach == "spectral":
            prepare_spectral_variable(
                var_name,
                overwrite=overwrite_preparation
            )
        else:
            prepare_raoq_variable(
                var_name,
                na_tolerance=na_tolerance,
                overwrite=overwrite_preparation
            )

    result_tables = []

    for pre_period, post_period in DATE_PAIRS:
        print("\n" + "=" * 60)
        print(
            f"{var_name}: "
            f"{pre_period} -> {post_period}"
        )
        print("=" * 60)

        if approach == "spectral":
            distance_path = spectral_distance_path(
                var_name,
                pre_period,
                post_period
            )

            binary_path = spectral_binary_path(
                var_name,
                pre_period,
                post_period
            )

            if overwrite_change or not os.path.exists(distance_path):
                spectral_change_detection(
                    var_name,
                    pre_period,
                    post_period
                )
            else:
                print(
                    "Using existing spectral distance raster:",
                    os.path.basename(distance_path)
                )

            if overwrite_binary or not os.path.exists(binary_path):
                spectral_binary_maps(
                    var_name,
                    pre_period,
                    post_period
                )
            else:
                print(
                    "Using existing spectral binary raster:",
                    os.path.basename(binary_path)
                )

            if make_outputs:
                result_tables.append(
                    spectral_maps_and_tables(
                        var_name,
                        pre_period,
                        post_period,
                        title_label
                    )
                )

        else:
            distance_path = raoq_distance_path(
                var_name,
                pre_period,
                post_period
            )

            binary_path = raoq_binary_path(
                var_name,
                pre_period,
                post_period
            )

            if overwrite_change or not os.path.exists(distance_path):
                raoq_change_detection(
                    var_name,
                    pre_period,
                    post_period
                )
            else:
                print(
                    "Using existing RaoQ distance raster:",
                    os.path.basename(distance_path)
                )

            if overwrite_binary or not os.path.exists(binary_path):
                raoq_binary_maps(
                    var_name,
                    pre_period,
                    post_period
                )
            else:
                print(
                    "Using existing RaoQ binary raster:",
                    os.path.basename(binary_path)
                )

            if make_outputs:
                result_tables.append(
                    raoq_maps_and_tables(
                        var_name,
                        pre_period,
                        post_period,
                        title_label
                    )
                )

    if not result_tables:
        return pd.DataFrame()

    return pd.concat(
        result_tables,
        ignore_index=True
    )


def run_all_variable_workflows(
    variables,
    approach,
    na_tolerance=0.3,
    prepare_data=True,
    overwrite_preparation=True,
    overwrite_change=True,
    overwrite_binary=True,
    make_outputs=True,
    continue_on_error=True
):
    results = {}
    errors = {}

    for index, var_name in enumerate(variables, start=1):
        print("\n" + "#" * 78)
        print(
            f"{str(approach).upper()} "
            f"{index}/{len(variables)}: {var_name}"
        )
        print("#" * 78)

        try:
            results[var_name] = run_variable_workflow(
                var_name=var_name,
                approach=approach,
                title_label=var_name,
                prepare_data=prepare_data,
                na_tolerance=na_tolerance,
                overwrite_preparation=overwrite_preparation,
                overwrite_change=overwrite_change,
                overwrite_binary=overwrite_binary,
                make_outputs=make_outputs
            )

        except Exception as error:
            errors[var_name] = {
                "error_type": type(error).__name__,
                "message": str(error),
                "traceback": traceback.format_exc()
            }

            print(
                f"FAILED {str(approach).upper()} {var_name}: "
                f"{type(error).__name__}: {error}"
            )

            if not continue_on_error:
                raise

    print("\n" + "=" * 78)
    print(f"{str(approach).upper()} WORKFLOW SUMMARY")
    print("=" * 78)
    print("Successful:", len(results))
    print("Failed:", len(errors))

    return results, errors


