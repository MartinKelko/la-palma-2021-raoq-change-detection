def try_load_lava_geom_in_crs(target_crs):
    try:
        lava_fc = ee.FeatureCollection(LAVA_ASSET)
        geom = lava_fc.union(1).geometry()
        geom_4326 = geom.getInfo()

        return transform_geom(
            "EPSG:4326",
            target_crs,
            geom_4326,
            precision=6
        )

    except Exception as e:
        print("WARNING: Lava polygon not available. Using full raster extent.")
        print("Reason:", repr(e))
        return None


def get_lava_geom_for_raster(path):
    _, meta = read_raster(path)

    target_crs = meta["crs"].to_string() if meta.get("crs") else None

    if target_crs is None:
        return None

    return try_load_lava_geom_in_crs(target_crs)


def summarize_binary_in_lava(binary, meta, lava_geom_proj=None):
    if lava_geom_proj is not None:
        mask_lava = geometry_mask(
            [lava_geom_proj],
            transform=meta["transform"],
            invert=True,
            out_shape=binary.shape
        )
        inside = mask_lava & np.isfinite(binary)
    else:
        inside = np.isfinite(binary)

    n_valid = int(np.sum(inside))
    n_change = int(np.sum(binary[inside] == 1))

    px_area = float(abs(meta["transform"].a * meta["transform"].e))
    area_ha = (n_change * px_area) / 10000.0
    share = 100.0 * n_change / n_valid if n_valid > 0 else np.nan

    return n_valid, n_change, share, area_ha


def _iter_outer_rings(geom_proj):
    if geom_proj is None:
        return

    gtype = geom_proj.get("type")
    coords = geom_proj.get("coordinates")

    if coords is None:
        return

    if gtype == "Polygon":
        yield coords[0]

    elif gtype == "MultiPolygon":
        for poly in coords:
            if poly:
                yield poly[0]


def draw_outline(
    ax,
    geom_proj,
    meta,
    color=OUTLINE_COLOR,
    linewidth=OUTLINE_LW,
    decimate=OUTLINE_DECIMATE
):
    if geom_proj is None:
        return

    inv = ~meta["transform"]

    for ring in _iter_outer_rings(geom_proj):
        ring = ring[::max(1, int(decimate))]

        xs = np.array([pt[0] for pt in ring], dtype="float64")
        ys = np.array([pt[1] for pt in ring], dtype="float64")

        cols, rows = inv * (xs, ys)
        ax.plot(cols, rows, color=color, linewidth=linewidth)


def plot_vertical_block(file_paths, lava_geom_proj, title):
    file_paths = [p for p in file_paths if p and os.path.exists(p)]

    if not file_paths:
        print("WARNING: Nothing to plot for:", title)
        return

    cmap = ListedColormap(["white", "black"])

    legend = [
        Patch(facecolor="white", edgecolor="black", label="0 = no change"),
        Patch(facecolor="black", edgecolor="black", label="1 = change"),
    ]

    n = len(file_paths)
    fig, axes = plt.subplots(n, 1, figsize=(6, 4 * n))

    if n == 1:
        axes = [axes]

    for ax, p in zip(axes, file_paths):
        b, meta_b = read_raster(p)

        ax.imshow(b, cmap=cmap, vmin=0, vmax=1)
        draw_outline(ax, lava_geom_proj, meta_b)

        ax.set_title(os.path.basename(p), fontsize=10)
        ax.axis("off")

    fig.suptitle(title, fontsize=15, fontweight="bold", y=1.02)
    fig.legend(handles=legend, loc="lower center", ncol=2, frameon=False)

    plt.tight_layout(rect=[0, 0.06, 1, 1])
    plt.show()


