def _iter_outer_rings(geom_proj):
    if geom_proj is None:
        return

    geom_type = geom_proj.get("type")
    coordinates = geom_proj.get("coordinates")

    if coordinates is None:
        return

    if geom_type == "Polygon":
        yield coordinates[0]

    elif geom_type == "MultiPolygon":
        for polygon in coordinates:
            if polygon:
                yield polygon[0]


def draw_outline(
    ax,
    geom_proj,
    meta,
    color,
    linewidth,
    linestyle="-",
    decimate=OUTLINE_DECIMATE
):
    if geom_proj is None:
        return

    inverse_transform = ~meta["transform"]

    for ring in _iter_outer_rings(geom_proj):
        ring = ring[::max(1, int(decimate))]

        xs = np.array(
            [point[0] for point in ring],
            dtype="float64"
        )

        ys = np.array(
            [point[1] for point in ring],
            dtype="float64"
        )

        cols, rows = inverse_transform * (xs, ys)

        ax.plot(
            cols,
            rows,
            color=color,
            linewidth=linewidth,
            linestyle=linestyle
        )


