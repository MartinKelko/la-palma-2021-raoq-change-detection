def same_grid(meta_a, arr_a, meta_b, arr_b):
    return (
        arr_a.shape == arr_b.shape
        and meta_a.get("crs") == meta_b.get("crs")
        and meta_a.get("transform") == meta_b.get("transform")
    )


def align_to_reference(
    src_arr,
    src_meta,
    ref_meta,
    resampling=Resampling.bilinear
):
    if src_meta.get("crs") is None or ref_meta.get("crs") is None:
        raise ValueError("Both source and reference rasters must have a CRS.")

    dst = np.full(
        (ref_meta["height"], ref_meta["width"]),
        np.nan,
        dtype="float32"
    )

    reproject(
        source=src_arr,
        destination=dst,
        src_transform=src_meta["transform"],
        src_crs=src_meta["crs"],
        dst_transform=ref_meta["transform"],
        dst_crs=ref_meta["crs"],
        src_nodata=np.nan,
        dst_nodata=np.nan,
        resampling=resampling
    )

    return dst


def read_pair_aligned(
    pre_path,
    post_path,
    resampling=Resampling.bilinear
):
    pre, meta_pre = read_tif(pre_path)
    post, meta_post = read_tif(post_path)

    print("PRE shape :", pre.shape)
    print("POST shape:", post.shape)

    if not same_grid(meta_pre, pre, meta_post, post):
        print("Grids differ -> aligning POST raster to PRE grid")

        post = align_to_reference(
            post,
            meta_post,
            meta_pre,
            resampling=resampling
        )

        print("POST aligned shape:", post.shape)

    return pre, post, meta_pre


