def mask_s2_sr_scl(img):
    scl = img.select("SCL")
    mask = (
        scl.neq(0)
        .And(scl.neq(1))
        .And(scl.neq(3))
        .And(scl.neq(8))
        .And(scl.neq(9))
        .And(scl.neq(10))
        .And(scl.neq(11))
    )
    return img.updateMask(mask).copyProperties(img, img.propertyNames())


def add_s2_aoi_cloud_fraction(img):
    scl = img.select("SCL")
    cloud = (
        scl.eq(3)
        .Or(scl.eq(8))
        .Or(scl.eq(9))
        .Or(scl.eq(10))
        .Or(scl.eq(11))
    )
    cloud_fraction = cloud.reduceRegion(
        reducer=ee.Reducer.mean(),
        geometry=analysis_geom,
        scale=S2_SCALE,
        maxPixels=1e9,
        tileScale=4
    ).get("SCL")
    return img.set("AOI_CLOUD_FRACTION", cloud_fraction)


