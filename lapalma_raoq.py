def multidim_raoq_data_preparation(
    combo_vars,
    na_tolerance=0.0,
    overwrite=True,
    window=None
):
    outputs = []
    skipped = []

    window_size = (
        MD_RAOQ_WINDOW
        if window is None
        else window
    )

    for period in PERIODS:
        output_path = md_raoq_path(
            combo_vars,
            period
        )

        if (
            os.path.exists(output_path)
            and not overwrite
        ):
            outputs.append(
                output_path
            )

            print(
                "Using existing RaoQ:",
                os.path.basename(output_path)
            )

            continue

        temporary_inputs = []

        try:
            temporary_inputs = (
                open_normalized_multidim_inputs(
                    period,
                    combo_vars
                )
            )

            spectralrao_multidim(
                temporary_inputs,
                output_path,
                window=window_size,
                distance=MD_RAOQ_DISTANCE,
                na_tolerance=na_tolerance
            )

            if not os.path.exists(
                output_path
            ):
                raise FileNotFoundError(
                    f"RaoQ output was not created: "
                    f"{output_path}"
                )

            outputs.append(
                output_path
            )

            print(
                "Saved RaoQ:",
                os.path.basename(output_path)
            )

        except Exception as error:
            skipped.append({
                "variables": combo_label(combo_vars),
                "period": period,
                "error_type": type(error).__name__,
                "reason": str(error),
            })

            print(
                f"SKIPPED RaoQ {combo_label(combo_vars)} "
                f"{period}: {type(error).__name__}: {error}"
            )

        finally:
            if temporary_inputs:
                cleanup_tmp_files(
                    temporary_inputs
                )

    return outputs, skipped


