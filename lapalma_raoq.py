def compute_metrics(
    ref_series,
    pred_series
):

    values = pd.DataFrame({
        "ref": pd.to_numeric(
            ref_series,
            errors="coerce"
        ),
        "pred": pd.to_numeric(
            pred_series,
            errors="coerce"
        )
    }).dropna()

    values = values[
        values["ref"].isin([0, 1])
        &
        values["pred"].isin([0, 1])
    ]

    n = len(values)

    if n == 0:
        return {
            "validation_points": 0,
            "TP": np.nan,
            "FP": np.nan,
            "TN": np.nan,
            "FN": np.nan,
            "OA": np.nan,
            "QD": np.nan,
            "AD": np.nan,
            "Total_disagreement": np.nan,
            "Pontius_check": np.nan,
            "TSS": np.nan,
        }

    # --------------------------------------------------------
    # Confusion matrix
    # --------------------------------------------------------

    tp = int((
        (values["pred"] == 1)
        &
        (values["ref"] == 1)
    ).sum())

    fp = int((
        (values["pred"] == 1)
        &
        (values["ref"] == 0)
    ).sum())

    tn = int((
        (values["pred"] == 0)
        &
        (values["ref"] == 0)
    ).sum())

    fn = int((
        (values["pred"] == 0)
        &
        (values["ref"] == 1)
    ).sum())

    # --------------------------------------------------------
    # Overall agreement
    # --------------------------------------------------------

    oa = (
        tp + tn
    ) / n

    # --------------------------------------------------------
    # Pontius disagreement
    #
    # For a binary classification:
    #
    # QD = |FP - FN| / n
    # AD = 2 * min(FP, FN) / n
    # TD = QD + AD
    # --------------------------------------------------------

    quantity_disagreement = (
        abs(fp - fn) / n
    )

    allocation_disagreement = (
        2 * min(fp, fn) / n
    )

    total_disagreement = (
        quantity_disagreement
        +
        allocation_disagreement
    )

    pontius_check = (
        oa
        +
        quantity_disagreement
        +
        allocation_disagreement
    )

    sensitivity = (
        tp / (tp + fn)
        if (tp + fn) > 0
        else np.nan
    )

    specificity = (
        tn / (tn + fp)
        if (tn + fp) > 0
        else np.nan
    )

    tss = (
        sensitivity
        +
        specificity
        -
        1
        if (
            pd.notna(sensitivity)
            and pd.notna(specificity)
        )
        else np.nan
    )

    direct_total_disagreement = (
        fp + fn
    ) / n

    if not np.isclose(
        total_disagreement,
        direct_total_disagreement,
        atol=1e-10
    ):
        raise ValueError(
            "Pontius total disagreement check failed."
        )

    if not np.isclose(
        pontius_check,
        1.0,
        atol=1e-10
    ):
        raise ValueError(
            "Pontius identity OA + QD + AD = 1 failed."
        )

    return {
        "validation_points": n,
        "TP": tp,
        "FP": fp,
        "TN": tn,
        "FN": fn,
        "OA": oa,
        "QD": quantity_disagreement,
        "AD": allocation_disagreement,
        "Total_disagreement": total_disagreement,
        "Pontius_check": pontius_check,
        "TSS": tss,
    }


