def build_multidim_combinations(
    variables=None,
    min_dimension=2,
    max_dimension=None,
    same_group_only=False,
):
    variables = list(
        MD_SELECTED_VARIABLES
        if variables is None
        else variables
    )

    if len(variables) < 2:
        raise ValueError(
            "At least two variables are required."
        )

    if max_dimension is None:
        max_dimension = len(variables)

    if min_dimension < 2:
        raise ValueError(
            "min_dimension must be at least 2."
        )

    if max_dimension > len(variables):
        raise ValueError(
            "max_dimension cannot exceed the number "
            "of selected variables."
        )

    output = []

    for dimension in range(
        min_dimension,
        max_dimension + 1
    ):
        for combo in combinations(
            variables,
            dimension
        ):
            combo = list(combo)

            if same_group_only:
                groups = {
                    MD_VARIABLE_GROUP[variable]
                    for variable in combo
                }

                if len(groups) > 1:
                    continue

            output.append(combo)

    return output


