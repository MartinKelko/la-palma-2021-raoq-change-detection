def parse_prediction_column(
    column_name
):
    """
    Supported examples:

    BIN_NDVI_11
    BIN_NDVI_21

    Final suffix:
        _11 = pair 1, raster band 1
        _21 = pair 2, raster band 1
    """

    name = str(
        column_name
    ).strip()

    if not name.startswith("BIN_"):
        return None

    name = name.replace(
        "BIN_",
        "",
        1
    )

    match = re.search(
        r"_(1|2)1$",
        name
    )

    if match is None:
        match = re.search(
            r"_(1|2)$",
            name
        )

    if match is None:
        return None

    pair_id = match.group(1)

    variable = name[
        :match.start()
    ]

    if pair_id == "1":
        pre_period = "PRE_1"
        post_period = "POST_1"

    else:
        pre_period = "PRE_2"
        post_period = "POST_2"

    return {
        "variable_raw": variable,
        "pair_id": pair_id,
        "pair": (
            f"{pre_period}_TO_{post_period}"
        ),
        "pre_period": pre_period,
        "pre_date": np.nan,
        "post_period": post_period,
        "post_date": np.nan,
    }


