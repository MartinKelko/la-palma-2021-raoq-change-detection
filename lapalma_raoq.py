def mask_s2_sr_scl(img):
    """Mask invalid, shadow, cloud, cirrus, and snow S2 SCL classes."""
    scl = img.select("SCL")

    mask = (
        scl.neq(0)
        .And(scl.neq(1))
        .And(scl.neq(3))
        .And(scl.neq(8))
        .And(scl.neq(9))
        .And(scl.neq(10))
        .And(scl.neq(11))
    )

    return img.updateMask(mask).copyProperties(img, img.propertyNames())


def add_nir_swir_bands(img):
    """Return S2 image with consistently named B8A, B11, and B12 bands."""
    return img.addBands(
        [
            img.select("B8A").rename("B8A"),
            img.select("B11").rename("B11"),
            img.select("B12").rename("B12"),
        ],
        overwrite=True,
    )


def db_to_linear(img):
    return ee.Image(10).pow(img.divide(10.0))


def linear_to_db(img):
    return img.log10().multiply(10.0)


def mask_s1_edges_ard(img, pol, threshold_db=-35):
    band = img.select(pol)
    return img.updateMask(band.gt(threshold_db))


def refined_s1_speckle_filter_simple(img, pol, radius_pixels=1):
    band_db = img.select(pol)
    band_linear = db_to_linear(band_db)

    filtered_linear = band_linear.focal_mean(
        radius=radius_pixels,
        kernelType="square",
        units="pixels",
    )

    filtered_db = linear_to_db(filtered_linear).rename(pol)

    return (
        img.addBands(filtered_db, overwrite=True)
        .copyProperties(img, img.propertyNames())
    )


def preprocess_s1_ard_like(img, pol):
    image = mask_s1_edges_ard(img, pol, threshold_db=-35)
    image = refined_s1_speckle_filter_simple(
        image,
        pol,
        radius_pixels=1,
    )
    return image.copyProperties(img, img.propertyNames())


def mask_landsat_l2_clouds(img):
    qa = img.select("QA_PIXEL")

