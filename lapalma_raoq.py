def triangle_threshold(values, nbins=TRIANGLE_NBINS):
    valid = np.asarray(values)
    valid = valid[np.isfinite(valid)]

    if valid.size < MIN_SAMPLES:
        return np.nan

    vmin = float(valid.min())
    vmax = float(valid.max())

    if not np.isfinite(vmin) or not np.isfinite(vmax) or vmin == vmax:
        return np.nan

    hist, edges = np.histogram(
        valid,
        bins=nbins,
        range=(vmin, vmax)
    )

    if hist.sum() == 0:
        return np.nan

    x = (edges[:-1] + edges[1:]) / 2.0
    y = hist.astype(float)

    peak = int(np.argmax(y))

    # Use the longer histogram tail.
    left_length = peak
    right_length = len(y) - peak - 1

    if right_length >= left_length:
        i1, i2 = peak, len(y) - 1
    else:
        i1, i2 = 0, peak

    x1, y1 = x[i1], y[i1]
    x2, y2 = x[i2], y[i2]

    denominator = np.hypot(y2 - y1, x2 - x1)

    if denominator == 0:
        return np.nan

    indices = np.arange(i1, i2 + 1)

    distances = np.abs(
        (y2 - y1) * x[indices]
        - (x2 - x1) * y[indices]
        + x2 * y1
        - y2 * x1
    ) / denominator

    return float(x[indices[int(np.argmax(distances))]])


def otsu_threshold(values):
    valid = np.asarray(values)
    valid = valid[np.isfinite(valid)]

    if valid.size < MIN_SAMPLES:
        return np.nan

    return float(threshold_otsu(valid))


def get_threshold(values, method="triangle"):
    valid = np.asarray(values)
    valid = valid[np.isfinite(valid)]

    if valid.size < 2:
        return np.nan

    method = str(method).strip().lower()

    if method == "triangle":
        return triangle_threshold(valid, TRIANGLE_NBINS)

    if method == "otsu":
        return float(threshold_otsu(valid))

    if method == "yen":
        return float(threshold_yen(valid))

    raise ValueError(
        f"Unknown threshold method: {method}. "
        "Use 'triangle', 'otsu', or 'yen'."
    )


def apply_threshold(arr, threshold):
    binary = np.full(arr.shape, np.nan, dtype="float32")
    valid = np.isfinite(arr)

    if np.isfinite(threshold):
        binary[valid] = (arr[valid] > threshold).astype("float32")

    return binary


