def compute_metrics(ref_series, pred_series):
    tmp = pd.DataFrame({
        "ref": ref_series,
        "pred": pred_series
    }).dropna()

    tmp = tmp[tmp["ref"].isin([0, 1]) & tmp["pred"].isin([0, 1])]

    n = len(tmp)

    if n == 0:
        return {
            "validation_points": 0,
            "TP": np.nan,
            "FP": np.nan,
            "TN": np.nan,
            "FN": np.nan,
            "OA": np.nan,
            "Kappa": np.nan,
            "TSS": np.nan,
            "QD": np.nan,
            "AD": np.nan,
        }

    tp = int(((tmp["pred"] == 1) & (tmp["ref"] == 1)).sum())
    fp = int(((tmp["pred"] == 1) & (tmp["ref"] == 0)).sum())
    tn = int(((tmp["pred"] == 0) & (tmp["ref"] == 0)).sum())
    fn = int(((tmp["pred"] == 0) & (tmp["ref"] == 1)).sum())

    oa = (tp + tn) / n

    p_ref_1 = (tp + fn) / n
    p_ref_0 = (tn + fp) / n
    p_pred_1 = (tp + fp) / n
    p_pred_0 = (tn + fn) / n

    pe = (p_ref_1 * p_pred_1) + (p_ref_0 * p_pred_0)
    kappa = (oa - pe) / (1 - pe) if (1 - pe) != 0 else np.nan

    sensitivity = tp / (tp + fn) if (tp + fn) > 0 else np.nan
    specificity = tn / (tn + fp) if (tn + fp) > 0 else np.nan

    tss = sensitivity + specificity - 1 if pd.notna(sensitivity) and pd.notna(specificity) else np.nan

    qd = abs((tp + fp) - (tp + fn)) / n
    ad = 2 * min(fp, fn) / n

    return {
        "validation_points": n,
        "TP": tp,
        "FP": fp,
        "TN": tn,
        "FN": fn,
        "OA": oa,
        "Kappa": kappa,
        "TSS": tss,
        "QD": qd,
        "AD": ad,
    }


