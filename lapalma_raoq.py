def md_variable_approach(variable):
    if variable not in MD_VARIABLE_APPROACH:
        raise KeyError(
            f"No approach registered for variable: {variable}"
        )
    return MD_VARIABLE_APPROACH[variable]


def md_variable_group(variable):
    if variable not in MD_VARIABLE_GROUP:
        raise KeyError(
            f"No group registered for variable: {variable}"
        )
    return MD_VARIABLE_GROUP[variable]


def md_combination_type(combo_vars):
    approaches = [
        md_variable_approach(variable)
        for variable in combo_vars
    ]

    ordered_approaches = []
    for approach in ["single_spectral", "raoq_classic"]:
        ordered_approaches.extend(
            [approach] * approaches.count(approach)
        )

    return " + ".join(
        MD_APPROACH_DISPLAY_NAMES[approach]
        for approach in ordered_approaches
    )


def md_combination_groups(combo_vars):
    group_names = {
        "bands": "Band",
        "indices": "Index",
    }

    return " + ".join(
        group_names[md_variable_group(variable)]
        for variable in combo_vars
    )


def md_combination_category(combo_vars):
    approaches = [
        MD_VARIABLE_APPROACH[variable]
        for variable in combo_vars
    ]
    groups = [
        MD_VARIABLE_GROUP[variable]
        for variable in combo_vars
    ]

    if all(
        approach == "single_spectral"
        for approach in approaches
    ):
        if all(group == "bands" for group in groups):
            return "SINGLE SPECTRAL BAND COMBINATIONS"
        if all(group == "indices" for group in groups):
            return "SINGLE SPECTRAL INDEX COMBINATIONS"
        return "SINGLE SPECTRAL BAND & INDEX COMBINATIONS"

    if all(
        approach == "raoq_classic"
        for approach in approaches
    ):
        if all(group == "bands" for group in groups):
            return "RAOQ CLASSIC BAND COMBINATIONS"
        if all(group == "indices" for group in groups):
            return "RAOQ CLASSIC INDEX COMBINATIONS"
        return "RAOQ CLASSIC BAND & INDEX COMBINATIONS"

    if all(group == "bands" for group in groups):
        return "SINGLE SPECTRAL + RAOQ CLASSIC BAND COMBINATIONS"

    if all(group == "indices" for group in groups):
        return "SINGLE SPECTRAL + RAOQ CLASSIC INDEX COMBINATIONS"

    return "SINGLE SPECTRAL + RAOQ CLASSIC MIXED COMBINATIONS"


