def _raoQ_from_window(values, min_valid):
    v = values[np.isfinite(values)]
    k = v.size

    if k < min_valid or k < 2:
        return np.nan

    x = np.sort(v.astype("float64"))
    idx = np.arange(k, dtype="float64")

    sum_upper = np.sum(x * (2.0 * idx - k + 1.0))
    sum_abs_all_pairs = 2.0 * sum_upper

    return float(sum_abs_all_pairs / (k * k))


def raoq_classic_raster(in_path, out_path, window=9, na_tolerance=0.3):
    arr, meta = read_tif(in_path)

    if window % 2 != 1:
        raise ValueError("window must be odd")

    win_n = window * window
    min_valid = int(np.ceil(win_n * (1.0 - na_tolerance)))
    footprint = np.ones((window, window), dtype=bool)

    out = generic_filter(
        arr,
        function=lambda w: _raoQ_from_window(w, min_valid=min_valid),
        footprint=footprint,
        mode="constant",
        cval=np.nan
    ).astype("float32")

    write_tif(out_path, out, meta)

    print("Saved RaoQ classic:", os.path.basename(out_path))
    return out_path


