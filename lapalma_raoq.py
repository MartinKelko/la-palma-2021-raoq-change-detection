def create_binary_map(
    distance_path,
    binary_path,
    method,
    factor,
    label,
    var_name=None
):

    if not os.path.exists(distance_path):
        raise FileNotFoundError(
            f"Missing continuous change raster: {distance_path}"
        )

    change_arr, meta = read_raster(
        distance_path
    )

    geometries = get_reference_geometries_for_raster(
        distance_path
    )

    try:
        threshold_raw, threshold_region = get_binary_threshold(
            arr=change_arr,
            meta=meta,
            method=method,
            lava_geom_proj=geometries["lava"],
            var_name=var_name
        )

    except Exception as error:
        print("\n" + "=" * 70)
        print(f"SKIPPED binary map: {label}")
        print(f"{type(error).__name__}: {error}")
        print("=" * 70)

        return None

    threshold_applied = (
        threshold_raw
        * float(factor)
    )

    binary = apply_threshold(
        change_arr,
        threshold_applied
    )

    output_info = write_binary(
    path=binary_path,
    arr=binary,
    meta=meta,
    create_sampling_copy=True
)

    print("\n" + "-" * 60)
    print("BINARY MAP:", label)
    print("-" * 60)

    if var_name is not None:
        print("Variable:", var_name)

    print(
        "Threshold calibration region:",
        threshold_region
    )

    print(
        "Threshold method:",
        method
    )

    print(
        "Raw threshold:",
        f"{threshold_raw:.6f}"
    )

    print(
        "Threshold factor:",
        factor
    )

    print(
        "Applied threshold:",
        f"{threshold_applied:.6f}"
    )

    print(
        "Saved:",
        os.path.basename(binary_path)
    )

    return {
        "binary_path": binary_path,
        "variable": var_name,
        "threshold_raw": threshold_raw,
        "threshold_factor": float(factor),
        "threshold_applied": threshold_applied,
        "threshold_method": method,
        "sampling_path": output_info["sampling_path"],
        "threshold_region": threshold_region
    }


