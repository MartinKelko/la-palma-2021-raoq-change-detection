def md_normalization_rule(variable):
    """
    Return normalization limits for any selected variable.
    """

    # Exact variable rule
    if variable in NORMALIZATION_RULES:
        vmin, vmax = NORMALIZATION_RULES[variable]

        return {
            "vmin": vmin,
            "vmax": vmax,
        }

    # Sentinel-1 family rule
    if variable.startswith("S1_"):
        if "S1" not in NORMALIZATION_RULES:
            raise KeyError(
                "Missing generic Sentinel-1 normalization rule: "
                "NORMALIZATION_RULES['S1']"
            )

        vmin, vmax = NORMALIZATION_RULES["S1"]

        return {
            "vmin": vmin,
            "vmax": vmax,
        }

    raise KeyError(
        f"No normalization rule found for '{variable}'. "
        f"Available rules: {list(NORMALIZATION_RULES.keys())}"
    )

def multidim_preprocessing(
    combo_vars,
    periods=None,
    verbose=True
):
    periods = (
        PERIODS
        if periods is None
        else periods
    )

    rows = []

    for var_name in combo_vars:

        rule = md_normalization_rule(
            var_name
        )

        for period in periods:

            raster_path = md_input_path(
                var_name,
                period
            )

            date_value = md_input_date(
                var_name,
                period
            )

            exists = os.path.exists(
                raster_path
            )

            rows.append({
                "variable": var_name,
                "period": period,
                "date": date_value,
                "input_path": raster_path,
                "exists": exists,
                "vmin": rule["vmin"],
                "vmax": rule["vmax"],
            })

            if verbose:
                print(
                    f"{var_name:25s} | "
                    f"{period:6s} | "
                    f"{date_value} | "
                    f"{'OK' if exists else 'MISSING'}"
                )

    dataframe = pd.DataFrame(
        rows
    )

    missing = dataframe[
        ~dataframe["exists"]
    ]

    if not missing.empty:
        raise FileNotFoundError(
            "Missing multidimensional input rasters:\n"
            + "\n".join(
                missing["input_path"].tolist()
            )
        )

    return dataframe


