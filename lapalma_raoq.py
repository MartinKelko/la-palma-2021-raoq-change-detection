def triangle_threshold(values, nbins=TRIANGLE_NBINS):
    v = values[np.isfinite(values)]

    if v.size < MIN_SAMPLES:
        return np.nan

    vmin = float(np.nanmin(v))
    vmax = float(np.nanmax(v))

    if not np.isfinite(vmin) or not np.isfinite(vmax) or vmin == vmax:
        return np.nan

    hist, edges = np.histogram(v, bins=nbins, range=(vmin, vmax))

    if hist.sum() == 0:
        return np.nan

    x = (edges[:-1] + edges[1:]) / 2.0
    y = hist.astype(float)

    i_peak = int(np.argmax(y))
    i1, i2 = i_peak, len(y) - 1

    x1, y1 = x[i1], y[i1]
    x2, y2 = x[i2], y[i2]

    denom = np.hypot(y2 - y1, x2 - x1)

    if denom == 0:
        return np.nan

    idx = np.arange(i1, i2 + 1)

    dist = np.abs(
        (y2 - y1) * x[idx]
        - (x2 - x1) * y[idx]
        + x2 * y1
        - y2 * x1
    ) / denom

    return float(x[idx[int(np.argmax(dist))]])


def apply_threshold(arr, thr):
    binary = np.full(arr.shape, np.nan, dtype="float32")
    m = np.isfinite(arr)

    if np.isfinite(thr):
        binary[m] = (arr[m] > thr).astype("float32")

    return binary


