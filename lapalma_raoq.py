# Raster utilities (read_tif, write_tif, absdiff_arrays, diff_arrays)
def read_tif(path, nodata_fallback=NODATA_OUT):
    """Read 1-band GeoTIFF -> float32; nodata (tag or fallback) -> NaN."""
    with rasterio.open(path) as src:
        arr = src.read(1).astype("float32")
        meta = src.profile.copy()
        nod = src.nodata

    if nod is not None and np.isfinite(nod):
        arr[arr == nod] = np.nan
    arr[arr == nodata_fallback] = np.nan

    return arr, meta


def write_tif(path, arr, meta, nodata=NODATA_OUT, dtype="float32", compress="deflate"):
    """Write 1-band GeoTIFF; NaN -> numeric nodata; enforce nodata metadata."""
    meta2 = meta.copy()
    meta2.pop("compress", None)  # prevent conflicts from old profiles
    meta2.update(count=1, dtype=dtype, nodata=float(nodata), compress=compress)

    out = arr.astype(dtype)
    out[~np.isfinite(out)] = float(nodata)

    with rasterio.open(path, "w", **meta2) as dst:
        dst.write(out, 1)

    return path


def absdiff_arrays(a, b):
    """Absolute difference (|a-b|) with NaN propagation."""
    out = np.abs(a - b)
    out[~np.isfinite(a) | ~np.isfinite(b)] = np.nan
    return out


def diff_arrays(a, b):
    """Signed difference (a-b) with NaN propagation."""
    out = (a - b)
    out[~np.isfinite(a) | ~np.isfinite(b)] = np.nan
    return out

