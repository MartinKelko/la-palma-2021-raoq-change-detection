def compute_metrics(ref_series, pred_series):
    tmp = pd.DataFrame({
        "ref": ref_series,
        "pred": pred_series
    }).dropna()

    tmp = tmp[tmp["ref"].isin([0, 1]) & tmp["pred"].isin([0, 1])]
    n = len(tmp)

    if n == 0:
        return {
            "validation_points": 0,
            "TP": np.nan,
            "FP": np.nan,
            "TN": np.nan,
            "FN": np.nan,
            "OA": np.nan,
            "Kappa": np.nan,
            "TSS": np.nan,
            "QD": np.nan,
            "AD": np.nan,
        }

    tp = int(((tmp["pred"] == 1) & (tmp["ref"] == 1)).sum())
    fp = int(((tmp["pred"] == 1) & (tmp["ref"] == 0)).sum())
    tn = int(((tmp["pred"] == 0) & (tmp["ref"] == 0)).sum())
    fn = int(((tmp["pred"] == 0) & (tmp["ref"] == 1)).sum())

    oa = (tp + tn) / n

    p_ref_1 = (tp + fn) / n
    p_ref_0 = (tn + fp) / n
    p_pred_1 = (tp + fp) / n
    p_pred_0 = (tn + fn) / n

    pe = (p_ref_1 * p_pred_1) + (p_ref_0 * p_pred_0)
    kappa = (oa - pe) / (1 - pe) if (1 - pe) != 0 else np.nan

    sensitivity = tp / (tp + fn) if (tp + fn) > 0 else np.nan
    specificity = tn / (tn + fp) if (tn + fp) > 0 else np.nan

    tss = (
        sensitivity + specificity - 1
        if pd.notna(sensitivity) and pd.notna(specificity)
        else np.nan
    )

    qd = abs((tp + fp) - (tp + fn)) / n
    ad = 2 * min(fp, fn) / n

    return {
        "validation_points": n,
        "TP": tp,
        "FP": fp,
        "TN": tn,
        "FN": fn,
        "OA": oa,
        "Kappa": kappa,
        "TSS": tss,
        "QD": qd,
        "AD": ad,
    }


def classify_md_column(col_name):
    name = col_name.upper()

    name = name.replace("BIN_", "")
    name = name.replace("RAO_MD_", "")
    name = name.replace("RAOQ_MD_", "")
    name = name.replace("RAOQ_MULTIDIM_", "")
    name = name.replace("RAO_MULTIDIM_", "")
    name = name.replace("MULTIDIM_", "")
    name = name.replace("MD_", "")

    for y in ["_2021", "_2022", "_2023", "_2024", "_2025"]:
        name = name.replace(y, "")

    band_combos = {
        "B8A_VIIRS_NT": "B8A + VIIRS_NT",
        "B8A_S1_VH": "B8A + S1_VH",
        "B8A_TIR": "B8A + Landsat_TIR",
        "VIIRS_NT_S1_VH": "VIIRS_NT + S1_VH",
        "VIIRS_NT_TIR": "VIIRS_NT + Landsat_TIR",
        "S1_VH_TIR": "S1_VH + Landsat_TIR",
    }

    index_combos = {
        "MIRBI_NBR": "MIRBI + NBR",
        "MIRBI_NHI_SWNIR": "MIRBI + NHI_SWNIR",
        "MIRBI_NHI_SWIR": "MIRBI + NHI_SWIR",
        "NBR_NHI_SWNIR": "NBR + NHI_SWNIR",
        "NBR_NHI_SWIR": "NBR + NHI_SWIR",
        "NHI_SWNIR_NHI_SWIR": "NHI_SWNIR + NHI_SWIR",
    }

    if name in band_combos:
        return "mixed platforms", "multiple sensors", band_combos[name], "band_pair", "RaoQ_multidimensional"

    if name in index_combos:
        return "Sentinel-2", "MSI", index_combos[name], "index_pair", "RaoQ_multidimensional"

    return "unknown", "unknown", name, "unknown", "RaoQ_multidimensional"


def oa_class(value):
    if pd.isna(value):
        return "not available"
    elif value < 0.40:
        return "low agreement"
    elif value <= 0.60:
        return "moderate agreement"
    elif value <= 0.75:
        return "good agreement"
    elif value <= 0.80:
        return "excellent agreement"
    else:
        return "almost perfect agreement"


def kappa_class(value):
    if pd.isna(value):
        return "not available"
    elif value < 0.00:
        return "poor agreement"
    elif value <= 0.20:
        return "slight agreement"
    elif value <= 0.40:
        return "fair agreement"
    elif value <= 0.60:
        return "moderate agreement"
    elif value <= 0.80:
        return "substantial agreement"
    else:
        return "almost perfect agreement"


def tss_class(value):
    if pd.isna(value):
        return "not available"
    elif value < 0.50:
        return "poor"
    elif value <= 0.60:
        return "moderate"
    elif value <= 0.80:
        return "good"
    else:
        return "excellent"


def disagreement_class(value):
    if pd.isna(value):
        return "not available"
    elif value < 0.10:
        return "very good"
    elif value <= 0.20:
        return "acceptable"
    else:
        return "problematic"


