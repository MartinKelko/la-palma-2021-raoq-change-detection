def get_top_n_from_collection(
    collection,
    n=2,
    allow_duplicate=False
):
    size = collection.size().getInfo()

    if size == 0:
        raise ValueError(
            f"No valid images found, but {n} required."
        )

    if size < n and not allow_duplicate:
        raise ValueError(
            f"Only {size} valid images found, but {n} required."
        )

    image_list = collection.toList(size)
    output = []

    for index in range(min(size, n)):
        image = ee.Image(image_list.get(index))

        output.append({
            "image": image,
            "date": image.get("acq_date").getInfo(),
            "id": image.id().getInfo()
        })

    while len(output) < n:
        print(
            "WARNING: Insufficient images; "
            "duplicating the last available image."
        )

        output.append(output[-1].copy())

    return output


