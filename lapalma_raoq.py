def mask_landsat_clouds(img):
    qa = img.select("QA_PIXEL")

    dilated_cloud = qa.bitwiseAnd(1 << 1).eq(0)
    cloud = qa.bitwiseAnd(1 << 3).eq(0)
    cloud_shadow = qa.bitwiseAnd(1 << 4).eq(0)
    snow = qa.bitwiseAnd(1 << 5).eq(0)

    mask = dilated_cloud.And(cloud).And(cloud_shadow).And(snow)

    return img.updateMask(mask)


def prepare_landsat_tir(img):
    lst_k = img.select("ST_B10").multiply(0.00341802).add(149.0)
    lst_c = lst_k.subtract(273.15).rename("Landsat_TIR")

    return lst_c.copyProperties(img, img.propertyNames())


