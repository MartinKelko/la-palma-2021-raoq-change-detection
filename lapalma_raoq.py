def add_s2_indices(img):
    b4 = img.select("B4")
    b6 = img.select("B6")
    b7 = img.select("B7")
    b8a = img.select("B8A")
    b11 = img.select("B11")
    b12 = img.select("B12")

    ndvi = b8a.subtract(b4).divide(
        b8a.add(b4)
    ).rename("NDVI")

    nbr = b8a.subtract(b12).divide(
        b8a.add(b12)
    ).rename("NBR")

    mirbi = b11.multiply(10).subtract(
        b12.multiply(9.8)
    ).add(2).rename("MIRBI")

    bais2 = (
        ee.Image(1)
        .subtract(
            (
                b6.multiply(b7).multiply(b8a)
                .divide(b4)
            ).sqrt()
        )
        .multiply(
            (
                b12.subtract(b8a)
                .divide(b12.add(b8a))
            ).sqrt()
        )
        .rename("BAIS2")
    )

    nhi_swnir = b11.subtract(b8a).divide(
        b11.add(b8a)
    ).rename("NHI_SWNIR")

    nhi_swir = b12.subtract(b11).divide(
        b12.add(b11)
    ).rename("NHI_SWIR")

    return img.addBands([
        ndvi,
        nbr,
        mirbi,
        bais2,
        nhi_swnir,
        nhi_swir
    ])


