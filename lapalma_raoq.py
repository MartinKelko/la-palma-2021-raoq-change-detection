def _raoQ_from_window(values, min_valid):
    valid = values[np.isfinite(values)]
    count = valid.size

    if count < min_valid or count < 2:
        return np.nan

    x = np.sort(valid.astype("float64"))
    indices = np.arange(count, dtype="float64")

    sum_upper = np.sum(
        x * (2.0 * indices - count + 1.0)
    )

    return float((2.0 * sum_upper) / (count * count))


def raoq_classic_raster(
    in_path,
    out_path,
    window=9,
    na_tolerance=0.3
):
    if window % 2 != 1:
        raise ValueError("window must be odd")

    arr, meta = read_tif(in_path)

    window_size = window * window
    min_valid = int(
        np.ceil(window_size * (1.0 - float(na_tolerance)))
    )

    footprint = np.ones((window, window), dtype=bool)

    output = generic_filter(
        arr,
        function=lambda values: _raoQ_from_window(
            values,
            min_valid=min_valid
        ),
        footprint=footprint,
        mode="constant",
        cval=np.nan
    ).astype("float32")

    write_tif(out_path, output, meta)

    print("Saved RaoQ classic:", os.path.basename(out_path))

    return out_path


