def normalize_fixed_range(arr, vmin, vmax, clip=True):
    out = arr.astype("float32").copy()
    m = np.isfinite(out)

    if clip:
        out[m] = np.clip(out[m], vmin, vmax)

    out[m] = (out[m] - vmin) / (vmax - vmin)
    return out


